import UIKit


extension Int {
    
    var cg: CGFloat {
        return CGFloat(self)
    }
}


extension Double {
    
    var cg: CGFloat {
        return CGFloat(self)
    }
}


extension Float {
    
    var cg: CGFloat {
        return CGFloat(self)
    }
}


extension CGFloat {
    
    var int: Int {
        return Int(self)
    }
    
    var double: Double {
        return Double(self)
    }
    
    var float: Float {
        return Float(self)
    }
    
}
